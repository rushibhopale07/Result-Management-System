        String coursename= (String)jComboBox1.getSelectedItem();
        String branch= (String)jComboBox2.getSelectedItem();
        String rollno=jTextField1.getText();
        String name=jTextField2.getText();
        String surname=jTextField3.getText();
        String gender= (String)jComboBox3.getSelectedItem();
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3306/rushi","root","1234");
            Statement st=con.createStatement();
            st.executeUpdate("insert into student values('"+rollno+"', '"+coursename+"','"+branch+"','"+name+"','"+gender+"','"+surname+"')");
            JOptionPane.showMessageDialog(null, "Data Update SuccessFully");
            setVisible(false);
            new adminIndex().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
