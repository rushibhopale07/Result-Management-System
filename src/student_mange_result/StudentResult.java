    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        String rollno=rntext.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3306/rushi","root","1234");
            Statement st=con.createStatement();
            ResultSet rs= st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+ rollno+"'");
            if(rs.next()){
                branchtext.setText(rs.getString(3));
                coursetext.setText(rs.getString(2));
                gtext.setText(rs.getString(5));
                nametext.setText(rs.getString(4));
                rntext.setText(rs.getString(1));
                surntext.setText(rs.getString(6));
                jTextField1.setText(rs.getString(8));
                jTextField2.setText(rs.getString(9));
                jTextField3.setText(rs.getString(10));
                jTextField4.setText(rs.getString(11));
                jTextField5.setText(rs.getString(12));
                
                int s1=Integer.parseInt(jTextField1.getText());
                int s2=Integer.parseInt(jTextField2.getText());
                int s3=Integer.parseInt(jTextField3.getText());
                int s4=Integer.parseInt(jTextField4.getText());
                int s5=Integer.parseInt(jTextField5.getText());
                int result=(s1+s2+s3+s4+s5);
                
                jTextField8.setText(String.valueOf(result));
                if(s1<35 || s2<35 || s3<35 || s4<35 || s5<35)
                {
                    jTextField6.setText("Fail");
                    jTextField7.setText("Better Luck Next Time");
                }
                else
                {
                    jTextField6.setText("Pass");
                    jTextField7.setText("Congratulations");
                }
            }

         
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
        
    }//GEN-LAST:event_formComponentShown

